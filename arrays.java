public class arrays{

	public static void main(String[] args){
		int[] numbers = {10,12,14,15};

		System.out.println("Here are your numbers: ");

		for (int i = 0; i < numbers.length; i++){
			System.out.print(numbers[i] + ", ");
		}
		System.out.println(" ");
		System.out.println("So Java works");
	}
}
